    public void setStudent(int index){
        txtStudentName.setText(students[index].getName());
        txtAdmin.setText(students[index].getadminNumber());
        txtCourse.setText(students[index].getCourse());
        txtGPA.setText(String.format("%.2f",students[index].computeGPA()));
        Border studentBorder = BorderFactory.createTitledBorder(
                "Student " + (scount+1) + " of " 
                + String.format("%d", getStudentCount()));
        pnlStudent.setBorder(studentBorder);
        mcount = 0;
        setModule(students[index], mcount);
        String type = students[index].getType();
        if (type.equals("International Student")){
            txtOther.setText("I am an international Student.");
        } else {
            txtOther.setText("I am a Local Student.");
        }
    }
    
    public void setModule(Student s, int index){
        txtCode.setText(s.getModule(index).getCode());
        txtModuleName.setText(s.getModule(index).getName());
        txtCreditUnit.setText(String.format("%d",s.getModule(index).getCreditUnits()));
        txtMarks.setText(String.format("%.2f",s.getModule(index).getMark()));
        Border moduleBorder = BorderFactory.createTitledBorder(
                "Module " + (mcount+1) + " of " + 
                String.format("%d",s.getModulesTaken().length));
        pnlModule.setBorder(moduleBorder);
    }
    
    public int getStudentCount(){
        int count = 0;
        for (int i=0; i< students.length; i++) {
            if (students[i] != null)
                count++;
        }
        return count;
    }

