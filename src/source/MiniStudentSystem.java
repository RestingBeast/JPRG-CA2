    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        String searchStr = txtSearch.getText();
        if (rbStudent.isSelected()) {
            for (int i = 0; i < getStudentCount(); i++) {
            if (searchStr.equals(students[i].getName())){
                scount = i;
                setStudent(scount);
                txtMessage.setText("Found the Student!!");
                return;
            }
        }
            txtMessage.setText("Cannot find the student \""+ searchStr +"\"!!");
        } else {
            double totalMarks = 0,averageMark;
            int totalStudents = 0;
            for (int i = 0; i < getStudentCount(); i++) {
                for (int j = 0; j < students[i].getModulesTaken().length; j++){
                    Module[] temp = students[i].getModulesTaken();
                    if (temp[j].getName().equals(searchStr)) {
                        totalMarks += temp[j].getMark();
                        totalStudents++;
                    }    
                }
            }
            if (totalStudents != 0) {
                averageMark = totalMarks / totalStudents;
                txtMessage.setText(
                    "There are " + totalStudents + " student(s) taking "
                    + searchStr + " module.\nThe average mark for " + searchStr
                    + " is " + String.format("%.2f", averageMark) + "."
                );
            } else {
                txtMessage.setText("No Student taking " + searchStr + ".");
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnStatisticActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStatisticActionPerformed
        int goodGPA = 0, badGPA = 0, count = 0;
        for (int i = 0; i < students.length; i++) {
            if (students[i] != null) {
                count++;
              if (students[i].computeGPA() > 3.5) {
                    goodGPA++;
                } else if  (students[i].computeGPA() < 1) {
                    badGPA++;
                }  
            }  
        }
        double percentGood = (double)goodGPA / count * 100;
        double percentBad = (double)badGPA / count * 100;
        String msg = "STATISTIC:\n----------\n\nThere are "
                + count + " students in total.\n\n";
        msg += "There is/are " + goodGPA + " student(s) getting "
                + "GPA greater than 3.5. This is " 
                + String.format("%.2f", percentGood) + "%.\n\n";
        msg += "There is/are " + badGPA + " student(s) getting "
                + "GPA less than 1.This is "
                + String.format("%.2f", percentBad) + "%.\n\n";
        txtMessage.setText(msg);
    }//GEN-LAST:event_btnStatisticActionPerformed

